    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        
        ControladorMaterial ctrlMaterial = new ControladorMaterial();
        
        try {
            
            int indice = tbl_materiales.getSelectedRow();
            String codigo = tbl_materiales.getValueAt(indice, 0).toString();
            
            ctrlMaterial.materialEliminar(codigo);
            btn_buscarActionPerformed(evt);
            JOptionPane.showMessageDialog(rootPane, "MATERIAL ELIMINADO CORRECTAMENTE");
            
        }
        
        catch (Exception err) {
            
            JOptionPane.showMessageDialog(rootPane, "ERROR: NO HA SELECCIONADO NINGUN REGISTRO PARA ELIMINAR");
            
        }
        
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_btn_eliminarActionPerformed

    private void btn_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modificarActionPerformed

        try {
            int indice = tbl_materiales.getSelectedRow();
            String codigo = tbl_materiales.getValueAt(indice, 0).toString();
        
            VentanaModificarMaterial vmm = new VentanaModificarMaterial(codigo);
            vmm.setVisible(true);
            vmm.setTitle("Modificar  -  Material Audiovisual");
            vmm.setLocationRelativeTo(null);
        
            Toolkit miPantalla = Toolkit.getDefaultToolkit();
            Image icovideo = miPantalla.getImage("src/img/icovideo.png");
            vmm.setIconImage(icovideo);
            
            
        }
        
        
        
        catch (Exception err) {
            JOptionPane.showMessageDialog(rootPane, "ERROR: NO HA SELECCIONADO NINGÚN REGISTRO PARA MODIFICAR");
                    
        
        }
        
        
        
        
        
    }//GEN-LAST:event_btn_modificarActionPerformed

