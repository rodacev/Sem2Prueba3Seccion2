    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
        
        ArrayList<Material>listaMateriales = new ArrayList<Material>();
        ControladorMaterial ctrlMaterial = new ControladorMaterial();
        
        try {
            
            String codigo = txt_codigo.getText();
            listaMateriales = ctrlMaterial.MaterialListar(codigo);
            
            DefaultTableModel tablaModelo = (DefaultTableModel)tbl_materiales.getModel();
            
            tablaModelo.setRowCount(0);
            
            for (Material x:listaMateriales) {
                
                Object[] registro = {
                    x.getCodigo(),
                    x.getNombre(),
                    x.getFormato(),
                    x.getDuracion(),
                    x.getCategoria(),
                    x.getAutor(),
                    x.isEstado()   
                };
                tablaModelo.addRow(registro);
            }
            
        }
        
        catch (Exception err) {
            
            System.out.println("ERROR: " + err.getMessage());
        }




        // TODO add your handling code here:
    }//GEN-LAST:event_btn_buscarActionPerformed

